    private void invHTblMouseClicked(java.awt.event.MouseEvent evt) {                                     
        TableSelector selector = new TableSelector();
        int index = selector.tableClick(invHTbl,invLTbl);
        invNumLbl.setText(String.valueOf(index));
        for(InvoiceHeader header: arrayOfHeaders){
            if(header.getNum() == index){
                invDateTxt.setText(String.valueOf(header.getInvDate()));
                custTxt.setText(header.getCustomer());
                totalCount.setText(String.valueOf(header.getInvoiceTotal()));
            }
        }
    }

