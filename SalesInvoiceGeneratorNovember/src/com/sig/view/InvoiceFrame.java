    private void addItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addItemActionPerformed
        AddNewItem newItem = new AddNewItem(null, true);
        newItem.setVisible(true);
        newItem.setLocationRelativeTo(null);

        for(InvoiceHeader header: arrayOfHeaders){
            if(header.getNum() == Integer.parseInt(invNumLbl.getText())){
                totalCount.setText(String.valueOf(header.getInvoiceTotal()));
                DefaultTableModel model = (DefaultTableModel) invHTbl.getModel();
                model.setValueAt(header.getInvoiceTotal(),invHTbl.getSelectedRow(),3);
            }
        }


    }//GEN-LAST:event_addItemActionPerformed

